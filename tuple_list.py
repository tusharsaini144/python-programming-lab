# Python program to add a tuple to list 

# Creating the List
myList = [9, 3, 1, 4]

# Printing the List 
print("Initially List : " + str(myList))

# Creating Tuple
myTuple = (2, 6)

# Adding the tuple to list
myList += myTuple

# Printing resultant List
print("List after Addition : " + str(myList))
